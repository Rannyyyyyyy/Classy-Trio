    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        // Get the table model for the "tableProduct".
        DefaultTableModel model = (DefaultTableModel) tableProduct.getModel();

        try {
            // Establish a connection to the database.
            Connection con = MyConnection.getConnection();

            // Create a statement object to execute SQL queries.
            Statement st = con.createStatement();

            // Execute a query to fetch all rows from the "product" table.
            ResultSet rs = st.executeQuery("select * from product");

            // Iterate through the result set to process each row.
            while (rs.next()) {
                // Add a new row to the table model with the fetched data.
                // Each column is retrieved using the column name from the database.
                model.addRow(new Object[]{
                    rs.getString("name"), // Get the product name.
                    rs.getString("quantity"), // Get the product quantity.
                    rs.getString("price"), // Get the product price.
                    rs.getString("expirationDate") // Get the product expiration date.
                });
            }
        } catch (Exception e) {
            // Display an error message if an exception occurs during database interaction.
            JOptionPane.showMessageDialog(null, e);
        }

        // Disable the "Update" button by default since no product is selected initially.
        btnUpdate.setEnabled(false);

    }//GEN-LAST:event_formComponentShown

