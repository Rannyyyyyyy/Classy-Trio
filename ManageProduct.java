    /**
     * Method to center text in the table cells.
     */
    private void centerTableText() {
        // Create a cell renderer for centering text.
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER); // Align text to center.

        // Loop through each column in the table and apply the renderer.
        for (int i = 0; i < tableProduct.getColumnCount(); i++) {
            tableProduct.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
    }

    /**
     * Method to center text in the table header.
     */
    private void centerTableHeader() {
        // Get the header of the table.
        JTableHeader header = tableProduct.getTableHeader();

        // Create a renderer for centering header text.
        DefaultTableCellRenderer headerRenderer = (DefaultTableCellRenderer) header.getDefaultRenderer();
        headerRenderer.setHorizontalAlignment(JLabel.CENTER); // Align header text to center.

        // Apply the renderer to the table header.
        header.setDefaultRenderer(headerRenderer);
    }

    /**
     * Method to validate fields based on the form type.
     *
     * @param formType - The type of form ("edit" or "new").
     * @return - `true` if validation fails (fields are missing), `false` if
     * validation passes.
     */
    private boolean validateFields(String formType) {
        // Validation for "edit" form: Check if name, price, and expiration date are filled in.
        if (formType.equals("edit")
                && !txtName.getText().equals("")
                && !txtPrice.getText().equals("")
                && !txtExpirationDate.getText().equals("")) {
            return false; // Fields are valid, no error.
        } else {
            // Validation for "new" form: Check if name, price, expiration date, and quantity are filled in.
            return !(formType.equals("new")
                    && !txtName.getText().equals("")
                    && !txtPrice.getText().equals("")
                    && !txtExpirationDate.getText().equals("")
                    && !txtQuantity.getText().equals(""));
        }
    }

