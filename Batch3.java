    private void tableBatch3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBatch3MouseClicked
        // TODO add your handling code here:
        // Get the selected row index from tableBatch3.
        int index = tableBatch3.getSelectedRow();

        // Retrieve the table model for tableBatch3.
        TableModel model = tableBatch3.getModel();

        // Get the "name" value from the selected row and set it to the txtName field.
        String name = model.getValueAt(index, 0).toString();
        txtName.setText(name);

        // Get the "quantity" value from the selected row.
        String quantity = model.getValueAt(index, 1).toString();

        // Initialize the totalQuantity variable to 0 for fresh computation.
        totalQuantity = 0;

        // Set the label to indicate "Add Quantity" for the quantity field.
        lblQuantity.setText("Add Quantity");

        // Parse the selected "quantity" value as an integer and store it in totalQuantity.
        totalQuantity = Integer.parseInt(quantity);

        // Get the "price" value from the selected row and set it to the txtPrice field.
        String price = model.getValueAt(index, 2).toString();
        txtPrice.setText(price);

        // Get the "expirationDate" value from the selected row and set it to the txtExpirationDate field.
        String expirationDate = model.getValueAt(index, 3).toString();
        txtExpirationDate.setText(expirationDate);

        // Disable the "Save" button since this operation is for updating an existing entry.
        btnSave.setEnabled(false);

        // Enable the "Update" button to allow editing of the selected record.
        btnUpdate.setEnabled(true);

    }//GEN-LAST:event_tableBatch3MouseClicked

